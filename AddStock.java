    private void btn_AddStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddStockActionPerformed
        // TODO add your handling code here:
        if(!this.CheckNameStock()){
            javax.swing.JOptionPane.showMessageDialog(rootPane, "Name Stock Invalid" );
        }else{
            // money more than > quantity * 10$ 
            if(new TreeTrader().GetMoney(username) >Integer.parseInt(txt_Quantity.getText()) *10 ){
                javax.swing.JOptionPane.showMessageDialog(rootPane, "Add success" );
                this.UpdateMoney(username);
                Stock st = new Stock();
                st.setName(txt_NameStock.getText());
                st.setUsername(username);
                st.setQuantity(Integer.parseInt(txt_Quantity.getText()));
                this.UpdateStock(st);
                txt_NameStock.setText("");
                txt_Quantity.setText("");
                
                lb_Money.setText(String.valueOf(new TreeTrader().GetMoney(username))+"$");
            }else{// not enough money 
                javax.swing.JOptionPane.showMessageDialog(rootPane, "Not enough money" );
            }          
        }
    }//GEN-LAST:event_btn_AddStockActionPerformed
    
       
