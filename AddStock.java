    private void UpdateStock(Stock st){
        if(new Stock().CheckStock(st)){
            new Stock().UpdateStockSQL(st);
        }else{
            new Stock().AddStockSQL(st);
        }
    }
    private void UpdateMoney(String user){
        float m = Float.parseFloat(txt_Quantity.getText()) * 10;
         String sql = "update [User] set Money -="+ m + "where username = '" +user+"'";                                                    
        ConnectionServer conn = new ConnectionServer();
        try {
            Connection cn = conn.getConnectionSQL();
            Statement stm = cn.createStatement();
            stm.executeUpdate(sql);
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
